let currentLocale = localStorage.getItem('locale') || 'tr';

export const i18n = {
    t(key, params = {}) {
        const locale = translations[currentLocale] || {};
        const fallbackLocale = translations.tr || {};

        let result = locale[key] ?? fallbackLocale[key];
        if (result === undefined || result === null) {
            const keys = key.split('.');
            let nested = locale;
            let nestedFallback = fallbackLocale;
            for (const k of keys) {
                nested = nested && typeof nested === 'object' ? nested[k] : undefined;
                nestedFallback = nestedFallback && typeof nestedFallback === 'object' ? nestedFallback[k] : undefined;
            }
            result = nested ?? nestedFallback ?? key;
        }

        if (typeof result !== 'string') return key;
        for (const [pKey, pValue] of Object.entries(params)) {
            result = result.replace(new RegExp(`\\{${pKey}\\}`, 'g'), pValue);
        }
        return result;
    },
    setLocale(locale) {
        if (translations[locale]) {
            currentLocale = locale;
            localStorage.setItem('locale', locale);
            this.updateDOM();
        }
    },
    getLocale() {
        return currentLocale;
    },
    updateDOM() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if ((el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') && el.hasAttribute('placeholder')) {
                el.placeholder = this.t(key);
            } else {
                el.innerText = this.t(key);
            }
        });

        document.documentElement.lang = currentLocale;
        const title = document.querySelector('title');
        const titleKey = title?.getAttribute('data-i18n');
        if (titleKey) document.title = this.t(titleKey);
    }
};
